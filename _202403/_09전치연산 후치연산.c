#include <stdio.h>

int main() {
    int x = 10;
    int y = 10;

    // ================================
    // Post-increment (후치 증가 연산자)
    // ----------------
    // 의미: 현재 값을 먼저 사용한 뒤, 나중에 1 증가시킨다.
    // 영어: Post-increment
    // 예: y = 10 → printf("%d", y++);  // 출력: 10, 그 후 y = 11
    // ================================
    printf("Post-increment (y++): %d\n", y++);  
    printf("After y++ 실행 후 y 값: %d\n\n", y);  

    // ================================
    // Pre-increment (전치 증가 연산자)
    // ----------------
    // 의미: 값을 먼저 1 증가시키고, 그 새로운 값을 사용한다.
    // 영어: Pre-increment
    // 예: x = 10 → printf("%d", ++x);  // 출력: 11, x = 11
    // ================================
    printf("Pre-increment (++x): %d\n", ++x);  
    printf("After ++x 실행 후 x 값: %d\n\n", x);  

    // ================================
    // Post-decrement (후치 감소 연산자)
    // ----------------
    // 의미: 현재 값을 먼저 사용한 뒤, 나중에 1 감소시킨다.
    // 영어: Post-decrement
    // 예: y = 11 → printf("%d", y--);  // 출력: 11, 그 후 y = 10
    // ================================
    printf("Post-decrement (y--): %d\n", y--);  
    printf("After y-- 실행 후 y 값: %d\n\n", y);  

    // ================================
    // Pre-decrement (전치 감소 연산자)
    // ----------------
    // 의미: 값을 먼저 1 감소시키고, 그 새로운 값을 사용한다.
    // 영어: Pre-decrement
    // 예: x = 11 → printf("%d", --x);  // 출력: 10, x = 10
    // ================================
    printf("Pre-decrement (--x): %d\n", --x);  
    printf("After --x 실행 후 x 값: %d\n\n", x);  

    return 0;
}
